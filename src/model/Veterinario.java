package model;

public class Veterinario {
	

	private String codigo;

	public Veterinario() {
		super();
	}

	public Veterinario(String codigo) {
		super();
		this.codigo = codigo;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	
	

}
